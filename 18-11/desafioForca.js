/*
Desenvolvimento de um Jogo da Forca em JavaScript

Desenvolva um jogo da forca simples utilizando apenas estruturas básicas de programação em JavaScript. O jogo deverá funcionar no console do navegador ou em um terminal. Não é permitido o uso de objetos ou classes. Utilize apenas variáveis, funções, condicionais, laços e arrays.

Requisitos:
Escolha da palavra secreta:

A palavra secreta deve ser definida diretamente no código como uma string.
Exibição do progresso do jogador:

Use um array para representar o progresso do jogador, com underscores (_) no lugar de letras ainda não adivinhadas.
Entrada do jogador:

O jogador deverá digitar uma letra por vez.
Verifique se a letra pertence à palavra secreta e atualize o progresso.
Número máximo de tentativas:

O jogador terá no máximo 6 erros antes de perder o jogo.
Mensagens de vitória ou derrota:

Mostre uma mensagem de vitória se o jogador descobrir a palavra antes de atingir o limite de tentativas.
Mostre uma mensagem de derrota se o limite de tentativas for atingido.
Fluxo do Jogo:
Exiba o progresso inicial da palavra (ex.: _ _ _ _ _).
Permita que o jogador insira letras até acertar toda a palavra ou atingir o limite de tentativas.
Informe ao jogador se a letra inserida está ou não na palavra.
Exiba a quantidade de tentativas restantes a cada jogada.
*/